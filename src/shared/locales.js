function makeT(lang) {
  return (key, vars) => {
    let s = (LOCALES[lang] && LOCALES[lang][key]) ?? LOCALES.zh[key] ?? key;
    if (vars) for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
    return s;
  };
}

if (typeof module !== 'undefined' && module.exports) module.exports = { LOCALES, makeT };
if (typeof window !== 'undefined') window.EpilogueI18n = { LOCALES, makeT };
